#!/usr/bin/env node
/**
 * Post-deploy changelog writer.
 * Reads deploy info from env/args, generates a git log summary,
 * and writes a changelog entry to Obsidian via Local REST API.
 *
 * Usage: node scripts/post-deploy-changelog.mjs <deployId> <commitHash>
 *
 * Requires:
 *   OBSIDIAN_REST_TOKEN — from Obsidian Local REST API plugin settings
 *   OBSIDIAN_REST_URL   — defaults to http://127.0.0.1:27124
 */

const deployId = process.argv[2] || 'unknown'
const commitHash = process.argv[3] || 'unknown'

const OBSIDIAN_URL = process.env.OBSIDIAN_REST_URL || 'http://127.0.0.1:27124'
const OBSIDIAN_TOKEN = process.env.OBSIDIAN_REST_TOKEN

if (!OBSIDIAN_TOKEN) {
  console.warn('OBSIDIAN_REST_TOKEN not set — skipping changelog write to Obsidian')
  console.log('Deploy succeeded. Set OBSIDIAN_REST_TOKEN to enable auto-changelog.')
  process.exit(0)
}

import { execSync } from 'child_process'

// Get today's date
const today = new Date().toISOString().split('T')[0]

// Get git log since last tag or last 10 commits
let gitSummary = ''
try {
  gitSummary = execSync('git log --oneline -10 --no-decorate', { encoding: 'utf-8' }).trim()
} catch {
  gitSummary = '(could not read git log)'
}

// Build the changelog note
const content = `# HeardChef Deploy — ${today}

**Deploy ID:** \`${deployId}\`
**Commit:** \`${commitHash}\`
**URL:** https://heardchef-app.netlify.app
**Unique:** https://${deployId}--heardchef-app.netlify.app

---

## Changes in this deploy

\`\`\`
${gitSummary}
\`\`\`

---

*Auto-generated by post-deploy-changelog.mjs*
`

const notePath = `Changelogs/${today}-heardchef-deploy.md`

try {
  const res = await fetch(`${OBSIDIAN_URL}/vault/${encodeURIComponent(notePath)}`, {
    method: 'PUT',
    headers: {
      'Content-Type': 'text/markdown',
      'Authorization': `Bearer ${OBSIDIAN_TOKEN}`,
    },
    body: content,
  })

  if (res.ok) {
    console.log(`Changelog written to Obsidian: ${notePath}`)
  } else {
    const errText = await res.text()
    console.warn(`Obsidian API returned ${res.status}: ${errText}`)
    console.log('Deploy succeeded but changelog was not written to Obsidian.')
  }
} catch (err) {
  console.warn(`Could not reach Obsidian REST API: ${err.message}`)
  console.log('Deploy succeeded. Obsidian may not be running — changelog skipped.')
}
